INCLUDE "gbhw.inc"

SECTION "start", ROM0[$0100]
	nop
	jp	inicio
	
	
	ROM_HEADER	ROM_NOMBC, ROM_SIZE_32KBYTE, RAM_SIZE_0KBYTE
	
	
inicio:
	nop
	di
	ld	sp, $ffff
	
inicializacion:
	ld	a, %11100100	;Pallete registering
	ld	[rBGP], a
	
	ld a, 0		;Setting coordinates
	ld [rSCX], a
	ld [rSCY], a
	
	call apaga_LCD
	
	ld	hl, MonoGB_tile_data
	ld	de, _VRAM
	ld	bc, $09A0
	
.bucle_carga:
	ld	a, [hl]
	ld	[de], a
	dec	bc
	ld	a, b
	or	c
	jr	z, .fin_bucle_carga
	inc	hl
	inc	de
	jr	.bucle_carga

.fin_bucle_carga:
	ld	hl, _SCRN0
	ld	de, 32*32
	ld	bc, MonoGB_map_data
.draw_mono:
	ld	a, [bc]
	ld	[hl], a
	dec	de
	ld	a, d
	or	e
	jp	z, .finito
	inc	hl
	inc	bc
	jp	.draw_mono
	
.finito:
	ld	a, LCDCF_ON|LCDCF_BG8000|LCDCF_BG9800|LCDCF_BGON|LCDCF_OBJ8|LCDCF_OBJOFF
	ld	[rLCDC], a	
	
bucle:
	ld a, %11101101
	ld [rP1], a
	ld a, [rP1]
	ld a, [rP1]
	ld a, [rP1]
	ld a, [rP1]
	cp %11101101
	jp nz, .skipLeft
	ld a, [rSCX]
	cp 0
	jp z, .skipLeft
	inc a
	ld [rSCX], a
	jp .VBlank
.skipLeft:
	cp %11101110
	jp nz, .VBlank
	ld a, [rSCX]
	cp 181
	jp z, .VBlank
	dec a
	ld [rSCX], a
.VBlank:
	ld	a, [rLY]
	cp	145
	jr	nz, .VBlank
.fin_vB
	jr	bucle
	
apaga_LCD:
	ld	a, [rLCDC]
	rlca
	ret	nc
	
.espera_VBlank:
	ld	a, [rLY]
	cp	145
	jr	nz, .espera_VBlank
		
 ; we are in VBlank, we turn off the LCD
    ld      a,[rLCDC]       ; in A, the contents of the LCDC 
    res     7,a             ; we zero bit 7 (on the LCD)
    ld      [rLCDC],a 
	
	ret
	
